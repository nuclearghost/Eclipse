#import "UIColor+Eclipse.h"

@implementation UIColor (Eclipse)
+ (UIColor*)eclipseMagentaColor {
    return UIColorFromRGB(0xD91A4F);
}

+ (UIColor*)eclipseRedColor {
    return UIColorFromRGB(0x8C0329);
}

+ (UIColor*)eclipseYellowColor {
    return UIColorFromRGB(0xDFD331);

}

+ (UIColor*)eclipseLightBlueColor {
    return UIColorFromRGB(0x037FA6);
    
}

+ (UIColor*)eclipseDarkBlueColor {
    return UIColorFromRGB(0x0A6D8C);
}

+ (UIColor*)eclipseGrayColor {
    return UIColorFromRGB(0x252525);
}
@end
